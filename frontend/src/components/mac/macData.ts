export function drawRandomCard(filter: MacCategory | 'all' = 'all', exclude?: number): MacCard {
  let pool = filter === 'all' ? MAC_DECK : MAC_DECK.filter(c => c.category === filter);
  if (exclude !== undefined && pool.length > 1) {
    pool = pool.filter(c => c.number !== exclude);
  }
  return pool[Math.floor(Math.random() * pool.length)];
}
