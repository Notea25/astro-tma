async def run() -> None:
    async with AsyncSessionLocal() as db:
        inserted = 0
        updated = 0
        for entry in ENTRIES:
            slug = entry["slug"]
            assert isinstance(slug, str)
            existing = await db.execute(
                select(GlossaryTerm).where(GlossaryTerm.slug == slug)
            )
            term = existing.scalar_one_or_none()
            if term is None:
                term = GlossaryTerm(
                    slug=slug,
                    title_ru=entry["title_ru"],
                    category=entry["category"],
                    short_ru=entry["short_ru"],
                    full_ru=entry["full_ru"],
                    related_slugs=entry.get("related_slugs", []),
                )
                db.add(term)
                inserted += 1
                print(f"  + {slug}")
            else:
                term.title_ru = entry["title_ru"]
                term.category = entry["category"]
                term.short_ru = entry["short_ru"]
                term.full_ru = entry["full_ru"]
                term.related_slugs = entry.get("related_slugs", [])
                updated += 1
                print(f"  ~ {slug}")
        await db.commit()
        print(f"\nDone: inserted={inserted}, updated={updated}")


if __name__ == "__main__":
    asyncio.run(run())
