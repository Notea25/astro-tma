async def run() -> None:
    async with AsyncSessionLocal() as db:
        inserted = 0
        updated = 0
        for sign, text in ENTRIES.items():
            result = await db.execute(
                select(Interpretation).where(
                    Interpretation.planet == "ascendant",
                    Interpretation.sign == sign,
                    Interpretation.house.is_(None),
                    Interpretation.aspect.is_(None),
                )
            )
            row = result.scalar_one_or_none()
            if row is None:
                db.add(Interpretation(
                    planet="ascendant",
                    sign=sign,
                    house=None,
                    aspect=None,
                    text_ru=text,
                ))
                inserted += 1
                print(f"  + ascendant/{sign}")
            else:
                row.text_ru = text
                updated += 1
                print(f"  ~ ascendant/{sign}")
        await db.commit()
        print(f"\nDone: inserted={inserted}, updated={updated}")


if __name__ == "__main__":
    asyncio.run(run())
