def _aspect_description_map(
    descriptions: dict[str, Any] | None,
) -> dict[tuple[str, str, str], dict[str, Any]]:
    items = (descriptions or {}).get("aspects") or []
    out: dict[tuple[str, str, str], dict[str, Any]] = {}
    for entry in items:
        if not isinstance(entry, dict):
            continue
        p1 = _planet_key(entry.get("p1"))
        p2 = _planet_key(entry.get("p2"))
        atype = _aspect_key(entry.get("type") or entry.get("aspect"))
        if p1 and p2 and atype:
            out[(p1, p2, atype)] = entry
            out[(p2, p1, atype)] = entry
    return out


def _render_items_section(
    c: canvas.Canvas,
    w: float,
    h: float,
    title: str,
    subtitle: str,
    items: list[tuple[str, str]],
) -> None:
    y = _draw_title(c, title, w, h, subtitle)
    for header, paragraph in items:
        y = _ensure_space(c, y, 88, w, h)
        c.setFillColor(GOLD)
        _set_font(c, True, 14)
        c.drawString(40, y, header)
        y -= 18
        c.setFillColor(TEXT)
        _set_font(c, False, 12)
        y = _wrap_paragraph(c, paragraph, 40, y, 82, 15, 58, w, h)
        y -= 12
    _draw_footer(c, w)
    c.showPage()


def _element_counts(planets: dict[str, dict[str, Any]]) -> dict[str, int]:
    counts = {element: 0 for element in ELEMENTS}
    for planet in planets.values():
        sign = _key(planet.get("sign"))
        for element, (_, signs) in ELEMENTS.items():
            if sign in signs:
                counts[element] += 1
                break
    return counts


def _estimate_page_counts(
    planets: dict[str, dict[str, Any]],
    houses: list[dict[str, Any]],
    aspects: list[dict[str, Any]],
    reading: str | None,
) -> tuple[int, int, int, int, int]:
    """Return (planets_start, houses_start, aspects_start, reading_start, total)."""
    # Fixed pages: cover=1, toc=1, key_points=1, wheel=1, elements=1 → start planets at 6
    planets_start = 6
    planet_items = [name for name in PLANET_ORDER if planets.get(name)]
    planet_pages = math.ceil(len(planet_items) / 4) if planet_items else 1

    houses_start = planets_start + planet_pages
    house_items = [h for h in houses if int(h.get("number") or 0)]
    house_pages = math.ceil(len(house_items) / 6) if house_items else 1

    aspects_start = houses_start + house_pages
    # Aspects: 1 header page + roughly 3 aspects per page after that
    grouped = [
        (atype, [a for a in aspects if _aspect_key(a.get("aspect")) == atype])
        for atype in ASPECT_ORDER
    ]
    grouped = [(atype, grp) for atype, grp in grouped if grp]
    total_aspect_chunks = sum(math.ceil(len(grp) / 3) for _, grp in grouped)
    aspect_pages = max(total_aspect_chunks, 1)

    reading_start = aspects_start + aspect_pages
    # Reading: estimate ~180 words per page
    text = str(reading or "").strip()
    word_count = len(text.split()) if text else 60
    reading_pages = max(math.ceil(word_count / 180), 1)

    total = reading_start + reading_pages  # +1 glossary page
    return planets_start, houses_start, aspects_start, reading_start, total


def generate_natal_pdf(
    user_name: str,
    birth_date: str,
    birth_time: str | None,
    birth_city: str,
    sun_sign: str,
    moon_sign: str,
    asc_sign: str | None,
    planets: dict[str, dict[str, Any]],
    houses: list[dict[str, Any]],
    aspects: list[dict[str, Any]],
    reading: str | None = None,
    descriptions: dict[str, Any] | None = None,
) -> bytes:
    """Generate a complete natal chart PDF and return it as bytes."""
    _register_fonts()
    buf = BytesIO()
    c = canvas.Canvas(buf, pagesize=A4)
    w, h = A4

    planets_start, houses_start, aspects_start, reading_start, total_hint = _estimate_page_counts(
        planets, houses, aspects, reading
    )

    page = 1
    planet_desc = (descriptions or {}).get("planets") or {}
    house_desc = (descriptions or {}).get("houses") or {}
    aspect_desc = _aspect_description_map(descriptions)

    def finish_page() -> None:
        nonlocal page
        _page_footer(c, w, page, total_hint)
        c.showPage()
        page += 1

    def title_page(title: str, subtitle: str) -> float:
        _new_page(c, w, h)
        c.setFillColor(GOLD)
        _set_font(c, True, 20)
        title_y = h - 82
        for line in _lines_for_width(c, title, w - 116, bold=True, size=20, max_lines=2):
            c.drawString(58, title_y, line)
