def _ensure_space(c: canvas.Canvas, y: float, needed: float, w: float, h: float) -> float:
    if y - needed >= 58:
        return y
    _draw_footer(c, w)
    c.showPage()
    _new_page(c, w, h)
    return h - 56


def _wrap_paragraph(
    c: canvas.Canvas,
    text: str,
    x: float,
    y: float,
    max_chars: int,
    line_h: int,
    bottom: int,
    w: float,
    h: float,
) -> float:
    words = str(text or "").split()
    line = ""
    for word in words:
        test = f"{line} {word}".strip()
        if len(test) > max_chars and line:
            c.drawString(x, y, line)
            y -= line_h
            line = word
            if y < bottom:
                _draw_footer(c, w)
                c.showPage()
                _new_page(c, w, h)
                y = h - 56
                c.setFillColor(TEXT)
                _set_font(c, False, 11)
        else:
            line = test
    if line:
        c.drawString(x, y, line)
        y -= line_h
    return y


def _lines(text: str, max_chars: int, max_lines: int | None = None) -> list[str]:
    words = str(text or "").replace("\n", " ").split()
    result: list[str] = []
    line = ""
    for word in words:
        test = f"{line} {word}".strip()
        if len(test) > max_chars and line:
            result.append(line)
            line = word
            if max_lines and len(result) >= max_lines:
                break
        else:
            line = test
    if line and (not max_lines or len(result) < max_lines):
        result.append(line)
    if max_lines and len(result) == max_lines and len(" ".join(words)) > len(" ".join(result)):
        result[-1] = result[-1].rstrip(" .") + "."
    return result


def _draw_wrapped_static(
    c: canvas.Canvas,
    text: str,
    x: float,
    y: float,
    max_chars: int,
    line_h: int,
    max_lines: int | None = None,
) -> float:
    for line in _lines(text, max_chars, max_lines):
        c.drawString(x, y, line)
        y -= line_h
    return y


def _lines_for_width(
    c: canvas.Canvas,
    text: str,
    max_width: float,
    *,
    bold: bool,
    size: float,
    max_lines: int | None = None,
) -> list[str]:
    font_name = FONT_BOLD if bold else FONT
    words = str(text or "").replace("\n", " ").split()
    result: list[str] = []
    line = ""
    for word in words:
        test = f"{line} {word}".strip()
        if line and c.stringWidth(test, font_name, size) > max_width:
            result.append(line)
            line = word
            if max_lines and len(result) >= max_lines:
                break
        else:
            line = test
    if line and (not max_lines or len(result) < max_lines):
        result.append(line)
    if max_lines and len(result) == max_lines and len(" ".join(words)) > len(" ".join(result)):
        result[-1] = result[-1].rstrip(" .,:;—-") + "."
    return result


def _limit_words(text: str, words: int) -> str:
    parts = str(text or "").split()
    if len(parts) <= words:
        return " ".join(parts)
    return " ".join(parts[:words]).rstrip(" .,:;—-") + "."


